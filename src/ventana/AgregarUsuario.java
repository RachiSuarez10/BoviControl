    private void btnGuardar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardar1ActionPerformed
        //recuperar datos para validar casillas

        String nombre, apellidop, apellidom, edad, telefono, direccion, correo, username, pass, cmb = "";
        int bandera = 0, nivel;

        nombre = txt_nombre.getText();
        apellidop = txtapellidop.getText();
        apellidom = txtapellidom.getText();
        edad = txtedad.getText();
        telefono = txttelefono.getText();
        direccion = txtdireccion.getText();
        correo = txtcorreo.getText();
        username = txtusername.getText();
        pass = txtpass.getText();
        nivel = cmbnivel.getSelectedIndex() + 1;

        if (nombre.equals("")) {
            txt_nombre.setBackground(color2);
            bandera++;
        }
        if (apellidop.equals("")) {
            txtapellidop.setBackground(color2);
            bandera++;
        }
        if (apellidom.equals("")) {
            txtapellidom.setBackground(color2);
            bandera++;
        }
        if (edad.equals("")) {
            txtedad.setBackground(color2);
            bandera++;
        }
        if (telefono.equals("")) {
            txttelefono.setBackground(color2);
            bandera++;
        }
        if (direccion.equals("")) {
            txtdireccion.setBackground(color2);
            bandera++;
        }
        if (correo.equals("")) {
            txtcorreo.setBackground(color2);
            bandera++;

        }
        if (username.equals("")) {
            txtusername.setBackground(color2);
            bandera++;
        }
        if (pass.equals("")) {
            txtpass.setBackground(color2);
            bandera++;
        }
        if (nivel == 1) {
            cmb = "Administrador";

        } else if (nivel == 2) {
            cmb = "Capturista";
        }
        if (bandera == 0) {

            try {
                Connection cn = Conexion.conectar();
                PreparedStatement pst = cn.prepareStatement(
                        "insert into usuarios values(?,?,?,?,?,?,?,?,?,?,?,?)");

                pst.setInt(1, 0);
                pst.setString(2, nombre);
                pst.setString(3, apellidop);
                pst.setString(4, apellidom);
                pst.setString(5, telefono);
                pst.setString(6, edad);
                pst.setString(7, direccion);
                pst.setString(8, correo);
                pst.setString(9, username);
                pst.setString(10, pass);
                pst.setString(11, cmb);
                pst.setString(12, "Activo");

                pst.executeUpdate();
                cn.close();

                JOptionPane.showMessageDialog(null, "El trabajador se registro con exito ");
                txt_nombre.setText("");
                txtapellidom.setText("");
                txtapellidop.setText("");
                txtcorreo.setText("");
                txtdireccion.setText("");
                txtedad.setText("");
                txttelefono.setText("");

            } catch (SQLException e) {

                System.err.println("Erro al guardar los datos de trabajador " + e);
                JOptionPane.showMessageDialog(null, "Error al dar de alta al trabajador");

            }

        } else {

            JOptionPane.showMessageDialog(null, "Debe llenar todo los campos");
            //regresar a balca las casillas
            txt_nombre.setBackground(Color.white);
            txtapellidom.setBackground(Color.white);
            txtapellidop.setBackground(Color.white);
            txtcorreo.setBackground(Color.white);
            txtdireccion.setBackground(Color.white);
            txtedad.setBackground(Color.white);
            txttelefono.setBackground(Color.white);
        }

    }//GEN-LAST:event_btnGuardar1ActionPerformed


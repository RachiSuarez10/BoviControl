    public TablaVenta() {
        initComponents();
              setSize(880, 400);
              
        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(
                    "select username,idventas,  no_arete, nombrecomprador, fecha_venta, precio_venta from ventas inner join usuarios using(idusuarios)");
            /* "select resultadotestpsico, resultadotestcondu from resultados inner join usuarios using(id_usuario) "
                    + "where id_usuario = '" + idusuario + "'");*/

            ResultSet rs = pst.executeQuery();

            if (rs.next()) {

                JtableVentas = new JTable(model);
                jScrollPane1.setViewportView(JtableVentas);
                
                model.addColumn("Realizada por");
                model.addColumn("ID venta");
                model.addColumn("ID Bovino");
                model.addColumn("Comprador");
                model.addColumn("Fecha de venta");
                model.addColumn("Precio de venta");

                while (rs.next()) {   //mientra encuentre datos

                    Object[] fila = new Object[6];

                    for (int i = 0; i < 6; i++) {

                        fila[i] = rs.getObject(i + 1);

                    }
                    model.addRow(fila);
                }
                cn.close();

            }
        } catch (SQLException e) {

            JOptionPane.showMessageDialog(null, "Error al llenar la tabla");
            System.err.println("Error al llenar la tabla " + e);
        }
    }

